// Switch case in typescript

const _crypto: string = "ETH"

switch(_crypto) {
    case "ETH":
        console.log('Time to buy ETH')
        break
    case "BNB":
        console.log('Time to buy BNB')
        break
    case "SOL":
        console.log('Time to buy SOL')
        break
    default:
        console.log('Wait ... ')
        break
}
